export type UiLocale = 'zh-CN' | 'zh-TW' | 'en-US' | 'ja-JP' | 'ko-KR' | 'th-TH' | 'vi-VN'

export const SUPPORTED_UI_LOCALES: Array<{ value: UiLocale; label: string; nativeName: string }> = [
  { value: 'zh-CN', label: '简体中文', nativeName: '简体中文' },
  { value: 'zh-TW', label: '繁體中文', nativeName: '繁體中文' },
  { value: 'en-US', label: 'English', nativeName: 'English' },
  { value: 'ja-JP', label: '日本語', nativeName: '日本語' },
  { value: 'ko-KR', label: '한국어', nativeName: '한국어' },
  { value: 'th-TH', label: 'ไทย', nativeName: 'ไทย' },
  { value: 'vi-VN', label: 'Tiếng Việt', nativeName: 'Tiếng Việt' }
]

export function normalizeUiLocale(value: unknown): UiLocale {
  if (
    value === 'zh-CN' ||
    value === 'zh-TW' ||
    value === 'en-US' ||
    value === 'ja-JP' ||
    value === 'ko-KR' ||
    value === 'th-TH' ||
    value === 'vi-VN'
  ) {
    return value
  }
  if (typeof value === 'string') {
    const locale = value.toLowerCase()
    if (locale === 'zh-hk' || locale === 'zh-mo' || locale === 'zh-hant') return 'zh-TW'
    if (locale === 'zh' || locale === 'zh-cn' || locale === 'zh-sg' || locale === 'zh-hans') return 'zh-CN'
    if (locale.startsWith('en')) return 'en-US'
    if (locale.startsWith('ja')) return 'ja-JP'
    if (locale.startsWith('ko')) return 'ko-KR'
    if (locale.startsWith('th')) return 'th-TH'
    if (locale.startsWith('vi')) return 'vi-VN'
  }
  return 'zh-CN'
}

export function localeNativeName(localeInput: unknown): string {
  const locale = normalizeUiLocale(localeInput)
  return SUPPORTED_UI_LOCALES.find((item) => item.value === locale)?.nativeName ?? '简体中文'
}

