function interpolate(template: string, vars?: Record<string, string | number | undefined>): string {
  if (!vars) return template
  return template.replace(/\{\{(\w+)\}\}/g, (_, key: string) => {
    const value = vars[key]
    return value === undefined ? '' : String(value)
  })
}

export function createUiTranslator(localeInput: unknown): UiTranslator {
  const locale = normalizeUiLocale(localeInput)
  return (key, vars) => interpolate(DICT[locale][key] ?? DICT['zh-CN'][key], vars)
}

