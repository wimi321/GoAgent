import assert from 'node:assert/strict'
import { readFileSync } from 'node:fs'
import { join } from 'node:path'
import test from 'node:test'

const root = process.cwd()
const read = (path) => readFileSync(join(root, path), 'utf8')

test('first launch uses a versioned, skippable AI and KataGo onboarding flow', () => {
  const types = read('src/main/lib/types.ts')
  const store = read('src/main/lib/store.ts')
  const app = read('src/renderer/src/App.tsx')
  const onboarding = read('src/renderer/src/features/onboarding/FirstRunOnboarding.tsx')

  assert.match(types, /onboardingVersion: number/)
  assert.match(types, /llmSetupStatus: LlmSetupStatus/)
  assert.match(types, /'unconfigured' \| 'verified' \| 'skipped' \| 'needs-attention'/)
  assert.match(store, /onboardingVersion: 0/)
  assert.match(store, /systemLocale = app\.getLocale\(\)/)
  assert.match(store, /reviewLanguage: defaultReviewLanguage\(\)/)
  assert.match(onboarding, /FIRST_RUN_ONBOARDING_VERSION = 1/)
  assert.match(onboarding, /detectSystemUiLocale\(\)/)
  assert.match(onboarding, /document\.documentElement\.lang = locale/)
  assert.match(onboarding, /continueLater/)
  assert.match(onboarding, /unchangedVerifiedConfiguration/)
  assert.match(onboarding, /llmSetupStatus: status/)
  assert.match(onboarding, /!apiKey\.trim\(\) && !dashboard\.systemProfile\.hasLlmApiKey/)
  assert.match(onboarding, /onboardingVersion: FIRST_RUN_ONBOARDING_VERSION/)
  assert.match(onboarding, /SUPPORTED_UI_LOCALES\.map/)
  assert.match(app, /settings\.onboardingVersion < FIRST_RUN_ONBOARDING_VERSION/)
})

