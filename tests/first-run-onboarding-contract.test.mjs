test('automatic KataGo benchmark is cancellable, bounded, and preempted by analysis', () => {
  const types = read('src/main/lib/types.ts')
  const store = read('src/main/lib/store.ts')
  const benchmark = read('src/main/services/katagoBenchmark.ts')
  const scheduler = read('src/main/services/analysis/scheduler.ts')
  const app = read('src/renderer/src/App.tsx')

  assert.match(types, /KataGoBenchmarkTaskStatus/)
  assert.match(store, /katagoAutoBenchmarkEnabled: true/)
  assert.match(benchmark, /timeoutMs: request\.timeoutMs \?\? 30_000/)
  assert.match(benchmark, /BenchmarkCancelledError/)
  assert.match(benchmark, /setSettings\(next\)/)
  assert.match(scheduler, /cancelKataGoBenchmark\(\)/)
  assert.match(app, /cancelKataGoBenchmark/)
  assert.match(app, /katagoAutoBenchmarkEnabled/)
  assert.match(app, /graphBusy \|\| territoryBusy \|\| liveAnalysis\.running/)
  assert.match(app, /benchmarkManualCancelRef/)
  assert.match(app, /if \(!benchmarkManualCancelRef\.current\) autoBenchmarkAttemptRef\.current = ''/)
  assert.match(app, /onlyIfNeeded: automatic/)
  assert.match(app, /status: `已复用缓存[\s\S]{0,220}visitsPerSecond: 0/)
  assert.match(app, /liveAnalysis\.visitsPerSecond > 0/)
})

