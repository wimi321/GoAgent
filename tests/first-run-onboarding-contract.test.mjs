test('KataGo model downloads pause without deleting partial data and resume with HTTP Range', () => {
  const assets = read('src/main/services/katago/katagoAssets.ts')
  const main = read('src/main/index.ts')
  const preload = read('src/preload/index.ts')
  const settingsPanel = read('src/renderer/src/features/settings/KataGoAssetsPanel.tsx')

  assert.match(assets, /Range: `bytes=\$\{resumeAt\}-`/)
  assert.match(assets, /createWriteStream\(tmp, \{ flags: resumed \? 'a' : 'w' \}\)/)
  assert.match(assets, /KataGoAssetInstallPausedError/)
  assert.match(assets, /stage: 'paused'/)
  assert.doesNotMatch(assets, /await unlink\(tmp\)\.catch\(\(\) => undefined\)\s*\n\s*const response/)
  assert.match(main, /katago-assets:cancel-install/)
  assert.match(preload, /cancelKataGoAssetInstall/)
  assert.match(settingsPanel, /installProgress\?\.stage === 'paused'/)
})
